package papplevaa.notepad.util;

/**
 * Enumeration representing the types of file chooser dialogs for choosing files.
 * This enum includes options for saving a file and opening a file.
 */
public enum ChooseFileDialogType {
    /**
     * Option indicating that the file dialog is for saving a file.
     */
    SAVE,
    /**
     * Option indicating that the file dialog is for opening a file.
     */
    OPEN
}
